// Function to get local projects (works immediately without Firebase)
export function getLocalProjects(): Project[] {
  return localProjects;
}

// Function to simulate adding a new project
export function addLocalProject(project: Omit<Project, 'id'>): Project {
  const newProject: Project = {
    ...project,
    id: `local-${Date.now()}`,
  };
  localProjects.push(newProject);
  return newProject;
}

// Get all photo projects
export function getLocalPhotos(): Project[] {
  return localProjects.filter(p => p.category === 'photo');
}

// Get photos for a specific category slug
export function getPhotosByCategory(categoryId: string): Project[] {
  return localProjects.filter(
    p => p.category === 'photo' &&
    Array.isArray(p.photoCategories) &&
    p.photoCategories.includes(categoryId)
  );
}

// Get stats for each category: id, photoCount, coverImage
export function getCategoryStats(): { id: string; photoCount: number; coverImage: string | undefined }[] {
  const photos = getLocalPhotos();
  // Collect all category slugs
  const allSlugs = new Set<string>();
  photos.forEach(p => (p.photoCategories || []).forEach(c => allSlugs.add(c)));

  return Array.from(allSlugs).map(id => {
    const inCategory = photos.filter(p => (p.photoCategories || []).includes(id));
    return {
      id,
      photoCount: inCategory.length,
      coverImage: inCategory[0]?.thumbnailUrl || inCategory[0]?.mediaUrl,
    };
  });
}

// Update a photo's categories (local only)
export function updatePhotoCategories(photoId: string, categories: string[]): boolean {
  const idx = localProjects.findIndex(p => p.id === photoId);
  if (idx === -1) return false;
  localProjects[idx] = { ...localProjects[idx], photoCategories: categories };
  return true;
}
